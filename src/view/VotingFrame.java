    private void btnVoteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVoteActionPerformed
        String selected = listCandidates.getSelectedValue();
        if (selected == null) {
            lblStatus.setText(bundle.getString("label.select_candidate"));
            return;
        }

        // Update kandidat & user di MongoDB
        MongoHelper.incrementVote(selected);
        MongoHelper.setUserVoted(currentUser.getUsername(), selected);

        // Update lokal
        currentUser.setVoted(true);
        currentUser.setVotedCandidate(selected);
        lblStatus.setText(bundle.getString("msg.vote_success"));

        // Optional: disable voting
        listCandidates.setEnabled(false);
        btnVote.setEnabled(false);

        // âœ… Kembali ke login frame
        javax.swing.JOptionPane.showMessageDialog(this, "Vote berhasil! Anda akan kembali ke halaman login.");
        this.dispose(); // tutup VotingFrame
        new LoginFrame().setVisible(true); // buka LoginFrame
    }//GEN-LAST:event_btnVoteActionPerformed

