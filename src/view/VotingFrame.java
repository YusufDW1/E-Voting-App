    private void loadCandidates() {
        candidateModel.clear();
        List<Candidate> candidates = MongoHelper.getAllCandidates();
        for (Candidate c : candidates) {
            candidateModel.addElement(c.getName());
        }
        listCandidates.setModel(candidateModel);
    }

    private void loadLanguageOptions() {
        cbLanguage.removeAllItems();
        cbLanguage.addItem("English");
        cbLanguage.addItem("Indonesia");

        cbLanguage.addActionListener(e -> applyTranslations());
    }

    private void applyTranslations() {
        String lang = cbLanguage.getSelectedItem().toString();
        Locale locale = lang.equals("Indonesia") ? new Locale("id") : new Locale("en");

        bundle = ResourceBundle.getBundle("i18n.messages", locale);
        setTitle(bundle.getString("voting.title"));
        btnVote.setText(bundle.getString("label.select_candidate"));
        lblStatus.setText(""); // kosongin status
    }

