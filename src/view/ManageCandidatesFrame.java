    private void loadCandidates() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama Kandidat");
        model.addColumn("Jumlah Vote");

        List<Candidate> candidates = MongoHelper.getAllCandidates();
        for (Candidate c : candidates) {
            model.addRow(new Object[]{c.getName(), c.getVoteCount()});
        }

        tableCandidates.setModel(model);
    }

