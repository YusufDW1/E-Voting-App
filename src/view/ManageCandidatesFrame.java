    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = txtCandidateName.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama kandidat tidak boleh kosong.");
            return;
        }

        // Cek apakah kandidat sudah ada
        boolean exists = MongoHelper.getAllCandidates().stream()
                .anyMatch(c -> c.getName().equalsIgnoreCase(name));

        if (exists) {
            JOptionPane.showMessageDialog(this, "Kandidat dengan nama ini sudah ada.");
            return;
        }

        // Simpan ke DB
        Candidate candidate = new Candidate(name);
        MongoHelper.addCandidate(candidate);

        // Refresh tabel
        loadCandidates();

        // Reset input
        txtCandidateName.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tableCandidates.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kandidat yang ingin dihapus.");
            return;
        }

        String name = tableCandidates.getValueAt(selectedRow, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus kandidat: " + name + "?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            MongoHelper.getCandidateCollection().deleteOne(Filters.eq("name", name));
            loadCandidates();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

